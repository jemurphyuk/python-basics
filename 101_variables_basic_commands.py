# Variables in python
# a variable is a box and we put stuff inside and give it a name
# Assign a variable; give box name, put stuff inside
box_pillows = 'pillows and stuff'

# Look inside a box
print(box_pillows)

# variable reassignment, changing box contents
box_pillows = "string"

print(box_pillows)

# important functions in python
# print (), strings direct, also variables
# type(), returns the data type of the variable inside
print(type(box_pillows))
# input (), input prompts user for an input