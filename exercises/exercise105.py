# create a functioning calculator

# +, -, /, *
from math import pi

def addition(n1, n2):
    return n1 + n2

def subtraction(n1, n2):
    return n1 - n2

def multiply(n1, n2):
    return n1 * n2

def divide(n1, n2):
    return n1 / n2

def a_of_circle(radius):
    return pi * radius ** 2

def a_of_traingle(base, height):
    return height * base * 0.5

def a_of_square(side):
    return side ** 2
